def Ex4(file):
    """MODIFICARE IL CONTENUTO DI QUESTA FUNZIONE PER SVOLGERE L'ESERCIZIO"""
    with open(file, 'r', encoding='UTF-8') as f:
        d = {}
        for l in f:
            l = l.strip().split(",")
            if 'Oggetto' in l:
                continue
            if l[0] not in d:
                d[l[0]] = [l[1], l[2]]
            if (l[0] in d) and (l[1] == d[l[0]][1]):
                d[l[0]][1] = l[2]
    return d
