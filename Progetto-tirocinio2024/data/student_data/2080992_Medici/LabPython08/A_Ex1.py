def A_Ex1(l):
    lettere=[]
    for c in range(ord('a'), ord('z')+1):
        lettere.append(chr(c))
    Max=0
    carattere=''
    for i in lettere:
        occ=0
        for j in l:
            if i in j:
                occ+=1
            if occ>=Max:

                Max=occ
                carattere=i
    return carattere
