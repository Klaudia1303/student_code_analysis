def Ex4(file):
    fin=open(file,encoding="UTF-8")
    campi=fin.readline()
    campi=campi.strip().split(',')
    diz={}
    for dati in fin:
        dati=dati.strip().split(',')
        if len(dati)==len(campi):
            if dati[0] not in diz:
                diz[dati[0]]=[dati[1],dati[2]]
            else:
                if diz[dati[0]][1]==dati[1]:
                    diz[dati[0]][1]=dati[2]

    fin.close()
    return diz
            
    
    
