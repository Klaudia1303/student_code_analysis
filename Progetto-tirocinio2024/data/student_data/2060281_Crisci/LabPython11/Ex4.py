def Ex4(file):
    f=open(file)
    fin=f.readline()
    righe=f.readlines()
    diz={}
    for i in range(len(righe)):
        riga=righe[i].strip().split(',')
        proprietario=riga[1]
        erede=riga[2]
        oggetto=riga[0]
        if oggetto not in diz:
            diz[oggetto]=[proprietario, erede]
        else:
            if proprietario == diz[oggetto][1]:
                diz[oggetto].pop()
                diz[oggetto].append(erede)
        print(diz)
    return diz

