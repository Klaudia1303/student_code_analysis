def A_Ex1(l):
    maxU = 0 # unicode del char pi√π frequente
    maxN = 0 # in quante stringhe esiste
    
    i = ord("a")
    while i <= ord("z"):
        c = chr(i)
        n = 0
        for j in l:
            if j.find(c) != -1:
                n += 1
        
        if n >= maxN and i > maxU:
            maxN = n
            maxU = i
        
        i += 1

    return chr(maxU)
            

