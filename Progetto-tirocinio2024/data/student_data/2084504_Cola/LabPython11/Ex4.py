def Ex4(file):
    """MODIFICARE IL CONTENUTO DI QUESTA FUNZIONE PER SVOLGERE L'ESERCIZIO"""
    f=open(file,encoding='UTF-8')
    f.readline()
    d={}
    for riga in f:
        s=riga.strip().split(',')
        if s[0] not in d:
            d[s[0]]=[s[1],s[2]]
        elif s[0] in d and d[s[0]][1]==s[1]:
            d[s[0]][1]=s[2]
    return d
            
            
               
        
    
