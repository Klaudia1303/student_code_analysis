def Ex4(file):
   f = open(file)
   r = f.readlines()
   diz = {}
   for l in range(1, len(r)):
       r[l] = r[l].strip().split(",")
       if r[l][0] not in diz:
           diz.update({r[l][0]:[r[l][1],r[l][2]]})
       else:
           if r[l][1] == diz[r[l][0]][1]:
              diz[r[l][0]][1] = r[l][2]              
   return diz
    
