def Ex4(file):
    f = open(file, 'r', encoding='UTF-8')
    t = f.read().split('\n')
    f.close()
    d = {}
    for i in range(1, len(t) - 1):
        s = t[i].strip().split(',')
        if s[0] in d:
            if d[s[0]][1] == s[1]:
                d[s[0]].remove(d[s[0]][1])
                d[s[0]].append(s[2])
        else:
            d[s[0]] = [s[1], s[2]]
    return d


