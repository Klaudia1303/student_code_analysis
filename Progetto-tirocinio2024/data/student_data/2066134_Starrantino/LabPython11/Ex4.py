def Ex4(file):
    """MODIFICARE IL CONTENUTO DI QUESTA FUNZIONE PER SVOLGERE L'ESERCIZIO"""
    with open(file, encoding='utf-8') as f:
        data = [line.strip().split(',') for line in f.readlines()][1:]
        out = {line[0]: [] for line in data}
        
        for line in data:
            obj, antenato, erede = line
            if len(out[obj]) == 0:
                out[obj] = [antenato, erede]  # init proprietario
            elif out[obj][1] == antenato:
                    out[obj][1] = erede
    return out

