n=int(input('lato del quadrato: '))
for i in range(1,n+1):
    if i==1 or i==n:
        print('*'*n)
    if 1<i<n/2 :
        print('*'+' '*(i-2)+'*'+' '*(n-2*(i-2)-4)+'*'+' '*(i-2)+'*')
    if n%2==0 and i==n/2:
        print('*'+' '*(i-2)+'*'+'*'+' '*(i-2)+'*')
        print('*'+' '*(i-2)+'*'+'*'+' '*(i-2)+'*')
    if n%2==1 and i==n/2+0.5:
        print('*'+' '*(i-2)+'*'+' '*(i-2)+'*')
    if (n%2==1 and n/2+0.5<i<n):
        print('*'+' '*(n-i-1)+'*'+' '*((2*i-n)-2)+'*'+' '*(n-i-1)+'*')
    if (n%2==0 and n/2+1<i<n):
        print('*'+' '*(n-i-1)+'*'+' '*((2*i-n)-2)+'*'+' '*(n-i-1)+'*')
