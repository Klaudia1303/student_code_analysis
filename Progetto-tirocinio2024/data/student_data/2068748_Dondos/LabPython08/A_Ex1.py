def A_Ex1(l):
    max_freq = 0
    max_char = ''
    for c in 'abcdefghijklmnopqrstuvwxyz':
        i = 0
        for s in l:
            if c in s:
                i += 1
            if i > max_freq:
                max_freq = i
                max_char = c
            elif i == max_freq and c > max_char:
                max_char = c
    return max_char

