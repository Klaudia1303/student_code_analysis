    first_line=f.readline().strip().split(',')
    column=first_line.index(str(anno))
    maxx=0
    obj=''
    for i in f:
        i=i.strip().split(',')
        if int(i[column])>maxx or int(i[column])==maxx:
            maxx=int(i[column])
            obj=i[0]
    return obj
