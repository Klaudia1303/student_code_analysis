def Ex4(file):
    """MODIFICARE IL CONTENUTO DI QUESTA FUNZIONE PER SVOLGERE L'ESERCIZIO"""
    fin=open(file, encoding="UTF-8")
    fin.readline()
    oggetti={}
    for r in fin:
        r=r.strip().split(",")
        if r[0] not in oggetti:
            oggetti[r[0]]=r[1:]
        elif oggetti[r[0]][1]==r[1]:
            oggetti[r[0]][1]=r[2]
    return oggetti
    
