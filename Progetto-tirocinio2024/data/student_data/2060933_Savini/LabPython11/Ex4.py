    d={}
    s=f.readline()
    for riga in f:
        riga=riga.strip().split(',')
        ogg=riga[0]
        ant=riga[1]
        erede=riga[2]
        if ogg not in d:
            d[ogg]=[ant, erede]
        elif ogg in d:
            r=d.get(ogg)
            if r[1]==ant:
                r.remove(r[-1])
                d[ogg].append(erede)
    return d


    
