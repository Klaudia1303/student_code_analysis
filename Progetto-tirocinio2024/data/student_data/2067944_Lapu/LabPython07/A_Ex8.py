    fine=''
    i=0
    if s1!='' and s2!='':
        while s1[i]==s2[i]:
            if s1[i]==s2[i]:
                fine=fine+s1[i]
            if len(fine)==len(s1) or len(fine)==len(s2):
                break
            i+=1
    return fine
