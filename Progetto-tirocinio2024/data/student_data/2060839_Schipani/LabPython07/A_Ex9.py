def A_Ex9(l):
    s=[]
    for i in l:
        k=0
        h=''
        a=0
        b=''
        for j in i:
            k=i.count(j)
            h=j
            if a<k:
                a=k
                b=h
            if a==k:
                if ord(h)<ord(b):
                    b=h
        s.append(b)
    return s
            

