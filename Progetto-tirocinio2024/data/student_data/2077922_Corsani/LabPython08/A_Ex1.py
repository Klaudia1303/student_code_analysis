def A_Ex1(l):
    appmax = 0
    car_max = "A"
    for i in range(len(l)):
        app = l[i]
        for j in range(len(app)):
            x = app[j]
            count = 0
            for q in range(len(l)):
                if x in l[q] and q != i and app[j].islower():
                    count +=1
            if count > appmax:
                appmax = count
                car_max = x
            elif appmax == count:
                if x > car_max:
                    car_max = x
    return car_max
