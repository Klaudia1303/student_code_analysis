def A_Ex8(file):
    res = {}
    with open(file, encoding="UTF-8") as f:
        table = [line.rstrip().split(",") for line in f.readlines()][1:]
        for line in table:
            if line[2] == "amici":
                for i in range(2):
                    res[line[i]] = res.get(line[i], set()) | {line[1-i]}
            else:
                for i in range(2):
                    res[line[i]] = res.get(line[i], set()) - {line[1-i]}
    for k in res.keys():
        res[k] = sorted(list(res[k]))
    return res
    
    
