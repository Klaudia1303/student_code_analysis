def A_Ex1(l):
    cs = set()
    for s in l:
        for c in s:
            if c.islower():
                cs.add(c)
    max_count = 0
    max_c = ""
    for c in cs:
        count = 0
        for s in l:
            if c in s:
                count += 1
        if count > max_count or (count == max_count and c > max_c):
            max_c = c
            max_count = count
    return max_c

