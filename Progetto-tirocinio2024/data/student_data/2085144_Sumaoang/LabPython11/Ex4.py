def Ex4(file):
    primo = {}
    ultimo = {}

    f1 = open(file, encoding="UTF-8")
    f2 = f1.readline()

    for x in f1:
        r = x.strip().split(",")
        oggetto = r[0]
        antenato = r[1]
        erede = r[2]
        if not oggetto in ultimo:
            primo[oggetto] = antenato 
            ultimo[oggetto] = erede
        elif ultimo[oggetto] == antenato:
            ultimo[oggetto] = erede
    
    t = {}
    for oggetto in primo:
        t[oggetto] = [primo[oggetto], ultimo[oggetto]]

    return t

