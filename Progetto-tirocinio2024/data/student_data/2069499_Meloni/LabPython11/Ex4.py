def Ex4(file):
    """MODIFICARE IL CONTENUTO DI QUESTA FUNZIONE PER SVOLGERE L'ESERCIZIO"""
    fin=open(file,encoding='UTF-8')
    fin.readline()

    diz={}
    for riga in fin:
        riga=riga.strip().split(',')
        if riga[0] not in diz:
            diz[riga[0]]=[riga[1],riga[2]]
        else:
            if riga[1]==diz[riga[0]][1]:
                diz[riga[0]].pop()
                diz[riga[0]].append(riga[2])

    return(diz)
    
    
    
    
