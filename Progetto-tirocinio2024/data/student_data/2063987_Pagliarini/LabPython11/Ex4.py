def Ex4(file):
    diz={}
    f=open(file,encoding='UTF-8')
    f.readline()
    for i in f:
        i=i.strip().split(',')
        if i[0] not in diz:
            diz[i[0]]=[]
            diz[i[0]].append(i[1])
            diz[i[0]].append(i[2])
        elif i[1] in diz[i[0]] and i[1]==diz[i[0]][1]:
            diz[i[0]][1]=i[2]
    return diz
        
            
        
        
