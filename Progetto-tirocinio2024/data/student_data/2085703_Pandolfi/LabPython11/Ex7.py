def Ex7(file):
    f = open(file, "r", encoding="UTF-8")
    riga = f.readline()

    d = {"invalidi" : 0, "domestici" : 0, "altri" : 0}

    pattern_generale = r"^([01]?[0-9][0-9]?|2[0-4][0-9]|25[0-5])\.([01]?[0-9][0-9]?|2[0-4][0-9]|25[0-5])\.([01]?[0-9][0-9]?|2[0-4][0-9]|25[0-5])\.([01]?[0-9][0-9]?|2[0-4][0-9]|25[0-5])$"    
    pattern_domestici = r"192.168.*\b"

    while len(riga) > 0:
        if re.search(pattern_generale, riga):
            if re.search(pattern_domestici, riga):
                d["domestici"] += 1
            else:
                d["altri"] += 1
        else:
            d["invalidi"] += 1

        riga = f.readline()

    f.close()

    return d

