def Ex4(file):
    d={}
    f=open(file,encoding="UTF-8")
    f.readline()
    for riga in f:
        dati=riga.split(",")
        oggetto=dati[0]
        dati=riga.strip().split(",")
        if oggetto not in d:
            l=[]
            l.append(dati[1])
            l.append(dati[2])
            d[oggetto]=l
        else:
            l2=d.get(oggetto)
            if l2[1]==dati[1]:
                l2.pop()
                l2.append(dati[2])
                d[oggetto]=l2
    f.close()
    return d
    
